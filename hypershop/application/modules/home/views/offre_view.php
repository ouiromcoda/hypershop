                       <h1>Nos offres aux africains</h1>
                       <p><b style="color:red;">OBJECTIF 1 : L’OFFRE GLOBALE POUR LA PARTICIPATION A LA COUPE DU MONDE 2018
AFRICAN VILLAGE a développé des produits pour répondre aux besoins des Africains lors de la Coupe du Monde 2018 :</b><br><br>
<b style="height:auto; width: 190px;background-color: #000000;color:#ffffff;padding: 5px;"> PACKAGES PERSONNALISES</b><br><br>
L’organisation d’un voyage pour la Coupe du Monde n’est pas une chose simple surtout.
Particulièrement si vous ne parlez pas la langue du pays organisateur (RUSSE). La compétition se déroulant dans un pays
de l’Est de l’Europe qui est la RUSSIE, et AFRICAN VILLAGE a opté pour faciliter le voyage des supporteurs Africains en
proposant des packages qui incluent toutes vos attentes.<br>
Les packages seront disponnibles dans toute l’Afrique.<br>
Notre objectif est de permettre au plus grand nombre d’Africains de pouvoir assister à cette Coupe du Monde FIFA organisée
en RUSSIE en toute simplicité.<br>
AFRICAN VILLAGE a réservé approximativement 1000 chambres dans la périphérie de la capitale MOSCOU pour accueillir
les visiteurs venus assister aux matches dans 5 des 12 stades sélectionnés.
<center><img class="img-responsive" src="<?php echo base_url();?>assets/images/offre.png" alt=""></center> <br><br>
<b style="color:red;">CES PACKAGES, COMME DÉTAILLÉS CI-APRÈS COMPRENNENT :</b><br>
<ol type="A">
  <li>Un billet d’avion aller/retour depuis leur pays d’origine judqu’en Russie</li>
  <li>Hébergement (sur un campus, dans les hôtels et villas)</li>
  <li>2 catégories de tickets (1 ou 2) pour les matches des équipes de leur choix qui jouent dans des stades à moins de trois
heures de route des sites d’AFRICAN VILLAGE (non valables pour tous les packages).</li>
  <li>Libre accès au village </li>
  <li>Visite des sites touristique de Moscou</li>
</ol>
<b>AFRICAN VILLAGE disposera de tickets afin de permettre à un grand nombre d’africains de se rendre aux stades.</b>
(Le système de paiement des tickets actuellement mis en place par la FIFA ne permet pas à la majorité d’Africains d’acheter des
tickets en ligne vu que le pourcentage de possession de cartes Visa est extrêmement bas en Afrique et une moyenne qualité de
l’acces à Internet à travers notre continent).<br><br>
Un package sera aussi disponible pour ceux qui veulent simplement prendre part à cette expérience unique en étant
présent en RUSSIE, lieu de l’évènement .<br>
La cible sera la diaspora et les européens souhaitant vivre cette expérience avec nous.
Des visites guidées sur les meilleurs sites touristiques de la RUSSIE seront aussi ouvertes aux invités pour des frais supplé-
mentaires.<br><br>
D’autres packages spécifiques seront ajoutés pour prendre en compte les exigences des supporters.
Toutes les commodités d’hébergement ont été mises à disposition et les clients seront logés dans les cités universitaires
et dans des hôtels en RUSSIE. <br><br>
<b style="color:red;">LA FOIRE-EXPO COMMERCIALE D’AFRICAN VILLAGE BUSINESS</b><br>

La Coupe du Monde 2018 en Russie représente une formidable opportunité pour les hommes d’affaires africains de se
rencontrer et d’échanger des idées en dehors des matches de phase finale de la Coupe du Monde et promouvoir les
produits et services africains à l’international.<br>
Cet évènement permettra à AFRICAN VILLAGE ouvrir la possibilité aux entreprises, aux institutions et aux ONG de présenter
leurs produits et services. De rencontrer des partenaires d’affaires, de participer à des rencontre B to B et de créer des
richesses pour l’Afrique.<br>
<center><img class="img-responsive" src="<?php echo base_url();?>assets/images/offre_1.png" alt=""></center><br><br>
<b style="color:red;"> L’AFRICAN VILLAGE</b><br>

Dans le but d’exposer les meilleurs atouts culturels, artistiques et musicaux de l’Afrique pendant cette Coupe du Monde
2018 en Russie, un espace de divertissement sera créé.<br>
<b>L’African Village sera situé au cœur du FIFA FAN Park de MOSCOU ou à provimité.</b><br>
Cet espace sera ouvert aux artistes, aux artisans, aux musiciens et aux services traiteurs (menus africains).
Les clients de l’AFRICAN VILLAGE auront la possibilité de déguster des mets et des boissons Africains en Russie .
Le service traiteur sera organisé sous la supervision de toutes les ambassades Africaines résidantes en RUSSIE.
L’essentiel lors d’une compétition de pareille envergure est de trouver des divertissements ou une occupation quand
votre pays ou le pays que vous supportez ne joue pas, ou même quand il n’y a pas de match du tout. AFRICAN VILLAGE
sera à la hauteur de ces sollicitations.<br>
Le public sera à même de voir d’autres matches sur écrans géant, écouter de la musique, voir des spectacles de danse et
des défilés de modes, à la fin de chaque journée. De participer à des concerts live avec divers artistes, parmi les plus
connus d’Afrique. Plusieurs autres activités seront organisées, et des activités parallèles seront destinées aux enfants.<br>
Les différentes traditions de l’Afrique seront célébrées par les africains pour le grand plaisir des visiteurs du village
Africain.
Une exposition d’art sera organisée par les artistes africains de renom sur le site.
Des télévisions et des radios seront présentes sur le site pour préparer et diffuser les reportages quotidiens sur ’’les news
de l’African village et la coupe du monde Russie 2018.<br>
L’accès au village sera gratuit pour les clients d’AFRICAN VILLAGE et chaque jour des passes d’accès seront vendus aux
visiteurs. <br><b>L’AFRICAN VILLAGE sera ouvert du 13 juin au 15 Juillet 2018.</b>
